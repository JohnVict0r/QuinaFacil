    private void valorApostaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorApostaKeyReleased
        if ((evt.getKeyCode() < 48 || evt.getKeyCode() > 58) && (evt.getKeyCode() != 8 && evt.getKeyCode() != 46)) {
            if (evt.getKeyCode() < 96 || evt.getKeyCode() > 105) {
                if (valorAposta.getText().length() > 0) {
                    valorAposta.setText(valorAposta.getText().
                            substring(0, valorAposta.getText().length() - 1));
                }
            }
        }
        if (evt.getKeyCode() == 44 || evt.getKeyCode() == 110) {
            valorAposta.setText(valorAposta.getText() + ".");
        }

    }//GEN-LAST:event_valorApostaKeyReleased

