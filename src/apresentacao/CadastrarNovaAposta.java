    private void numeroSelecionado(JRadioButton botaoRadio, JButton numeroDoBotao) {
        int quantidadeNumerosEscolhidos = Integer.parseInt(qtdNumerosSelecionados.getText());
        if (quantidadeNumerosEscolhidos < 29) {
            if (botaoRadio.isSelected()) {
                numerosEscolhidos.setText(numerosEscolhidos.getText() + numeroDoBotao.getText() + "-");
                quantidadeNumerosSelecionados = quantidadeNumerosSelecionados + 1;
                qtdNumerosSelecionados.setText(String.valueOf(quantidadeNumerosSelecionados));
                numeroDoBotao.setForeground(Color.RED);
            } else {
                quantidadeNumerosSelecionados = quantidadeNumerosSelecionados - 1;
                qtdNumerosSelecionados.setText(String.valueOf(quantidadeNumerosSelecionados));
                numeroDoBotao.setForeground(Color.BLACK);
                numerosEscolhidos.setText(numerosEscolhidos.getText().replaceAll(numeroDoBotao.getText() + "-", ""));
            }
        }

        if (quantidadeNumerosEscolhidos == 29) {
            if (botaoRadio.isSelected()) {
                numerosEscolhidos.setText(numerosEscolhidos.getText() + numeroDoBotao.getText());
                quantidadeNumerosSelecionados = quantidadeNumerosSelecionados + 1;
                qtdNumerosSelecionados.setText(String.valueOf(quantidadeNumerosSelecionados));
                numeroDoBotao.setForeground(Color.RED);
            } else {
                quantidadeNumerosSelecionados = quantidadeNumerosSelecionados - 1;
                qtdNumerosSelecionados.setText(String.valueOf(quantidadeNumerosSelecionados));
                numeroDoBotao.setForeground(Color.BLACK);
                numerosEscolhidos.setText(numerosEscolhidos.getText().replaceAll(numeroDoBotao.getText(), ""));
            }
        }
        
        if(quantidadeNumerosEscolhidos >= 30){
            botaoRadio.setSelected(false);
            JOptionPane.showMessageDialog(null, "Selecione apenas 30 números!");
        }
    }

    private void carregarConcursos() {
        try {
            List<Concurso> concursos = cdao.listarConcursos();
            for (Concurso c : concursos) {

                listaConcursos.addItem(c);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel mostrar os concursos!");

        }
    }

